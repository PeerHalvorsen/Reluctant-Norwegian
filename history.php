<h3>Oldest Fermented Beverage?</h3>
<p>There is still considerable debate, but there is some indication that mead may be the oldest fermented alcoholic beverage. 
Residual samples have been found in Northern China as early as 7000-6500 B.C. and in Europe as early as 2800-1800 B.C. The
earliest surviving description is found in the Indian Rigveda dating from 1700-1100 B.C. Mead has been a traditional beverage
in many parts of Scandinavia, Russia, and Ethiopia (where it is called tej).</p>

