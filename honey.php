<h3> The Essence of Mead</h3>
<p>The essence of all meads is honey. Without it you would not have mead. Many people are under the impression that mead is
like beer. This could not be further from the truth. In fact it is closer to wine in terms of character, but the truth is that
mead is mead. It is it's own thing with a richness of it's own.</p>

<h3>Varietals</h3>
<p>The type of honey that is used  is integral in forming the initial character of the honey. Varietals are honeys that have been 
created from the nectar of only one source. For instance buckwheat honey has been created by bees feeding off of solely buckweat 
flowers.</p>

<h3>Vintage</h3>
<p>Like grapes honeys have vintage, meaning that the year or season that the honey was made is different from the previous year.
This is due to a variety of factors, which is too complicated for this page. Suffice to say that no two meads are the same!
For further information see our resources page.</p>

